#Faça um programa que solicite dois números ao usuário e exiba o maior deles.

numero1=int(input("Digite o primeiro número:"))
numero2=int(input("Digite o segundo número:"))
if numero1>numero1:
    print(f"O número {numero1} é maior que {numero2}")
elif numero1==numero2:
    print(f"Voce digitou o mesmo valor {numero1} para ambos!")  
else:
    print(f"O número {numero2} é maior que {numero1}")   